const EVENT_COUNTS = Object.fromEntries(
  Object.entries(EVENT_SETS).map(([k, s]) => [k, s.size])
);

function isSkipDay(dateStr, skipConfig = {}) {
  for (const [key, set] of Object.entries(EVENT_SETS)) {
    if (skipConfig[key] && set.has(dateStr)) return true;
  }
  return false;
}

